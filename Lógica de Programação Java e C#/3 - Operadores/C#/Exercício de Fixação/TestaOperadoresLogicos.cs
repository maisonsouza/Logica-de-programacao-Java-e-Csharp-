class TestaOperadoresLogicos
{
    static void Main()
    {
        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;
        System.Console.WriteLine(a > b & c > d);
        System.Console.WriteLine(a < b && c > d);
        System.Console.WriteLine(a > b & c < d);
        System.Console.WriteLine(a < b && c < d);
        System.Console.WriteLine(a > b || c > d);
        System.Console.WriteLine(a < b | c > d);
        System.Console.WriteLine(a > b || c < d);
        System.Console.WriteLine(a < b | c < d);
        System.Console.WriteLine(a > b ^ c > d);
        System.Console.WriteLine(a < b ^ c > d);
        System.Console.WriteLine(a > b ^ c < d);
        System.Console.WriteLine(a < b ^ c < d);

    }
}