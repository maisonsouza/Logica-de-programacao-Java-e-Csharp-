class UseOperadoresDeAtribuicao{
	static void Main(){
		int x=5;
		System.Console.WriteLine(x);
		x += 10;
		System.Console.WriteLine(x);
		x -= 3;
		System.Console.WriteLine(x);
		x *= 4;
		System.Console.WriteLine(x);
		x /= 8;
		System.Console.WriteLine(x);
		x %= 5;
		System.Console.WriteLine(x);
		x++;
		System.Console.WriteLine(x);
		x--;
		System.Console.WriteLine(x);
	}
}
	